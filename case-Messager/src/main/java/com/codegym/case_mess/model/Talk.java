package com.codegym.case_mess.model;

public class Talk {
    private int id;
    private String content;
    public Talk(){}

    public Talk(int id, String content) {
        this.id = id;
        this.content = content;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
